def main():
    """
    ダブルコート3つでdocstringとなります
    ここにmain関数の説明を記述します。
    そんな感じです。
    """
    # 通常のコメントは#を使います。
    # 通常の文と同様にインデントを合わせる必要があります。
    x = 'text'
    print(x) # 'text'が出力される
    y = 100
    print(y) # 100が出力される
    print(x, y) # text 100が出力される
 
if __name__ == "__main__":
    main() # もちろん、文の後に書くことができます
